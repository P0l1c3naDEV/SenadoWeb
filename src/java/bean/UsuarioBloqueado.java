package bean;

import java.util.Date;

/**
 *
 * @author muril
 */
public class UsuarioBloqueado {
    private String email;
    private Date date;
    private int count;
    
    
    
    public UsuarioBloqueado(String email, Date date, int count) {
        this.email = email;
        this.date = date;
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
}
